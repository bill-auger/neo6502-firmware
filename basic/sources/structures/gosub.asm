		.section code

; ************************************************************************************************
;
;										GOSUB command
;
; ************************************************************************************************

Command_GOSUB:	;; [gosub]
		ldx 	#0
		jsr 	EXPEvalInteger16 			; get line number
		lda 	#STK_GOSUB
		jsr 	StackOpen 
		jsr 	STKSaveCodePosition 		; save return address on stack.
		jmp 	GotoR0

; ************************************************************************************************
;
;										RETURN command
;
; ************************************************************************************************

Command_RETURN:	;; [return]
		lda 	#STK_GOSUB
		jsr 	StackCheckFrame
		jsr 	STKLoadCodePosition 		; return
		jsr 	StackClose		 			
		rts

		.send code

