;										LOCALE Command
;
; ************************************************************************************************

		.section code

Command_LOCALE:	;; [locale]
		ldx 	#0 							; get the string
		jsr 	EXPEvalString
		lda 	(zTemp0) 					; check length is 2.
		cmp 	#2
		bne 	_CLRange
		phy
		ldy 	#1  						; copy the string text into the parameters.
		lda 	(zTemp0),y
		sta 	ControlParameters+0
		iny
		lda 	(zTemp0),y
		sta 	ControlParameters+1
		ply
		.DoSendMessage 						; call set locale
		.byte 	1,6
		.DoWaitMessage		
		rts

_CLRange:
		.error_range

		.send code

