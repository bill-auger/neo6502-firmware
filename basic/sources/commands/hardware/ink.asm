		.section code

Command_INK:	;; [ink]
		jsr 	InkGetColour
		ora 	#$80
		jsr 	WriteCharacter
		lda 	(codePtr),y
		cmp 	#KWD_COMMA
		bne 	_CIExit
		jsr 	WriteCharacter
		iny
		jsr 	InkGetColour
		ora 	#$90
		jsr 	WriteCharacter
_CIExit:		
		rts

InkGetColour:
		ldx 	#0
		jsr 	EXPEvalInteger8
		cmp		#16
		bcs 	_IGCRange
		rts
_IGCRange:
		.error_range		

		.send code

