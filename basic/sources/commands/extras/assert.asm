;										ASSERT Command
;
; ************************************************************************************************

		.section code

Command_ASSERT:	;; [assert]
		ldx 	#0
		jsr 	EXPEvalNumber 				; get a number to assert
		jsr 	CheckIfZero
		beq 	_CAFail 					; if so, the assert fails.
		rts
_CAFail:		
		.error_assert

; ************************************************************************************************
;
;										Check if TOS is zero
;
; ************************************************************************************************
		
CheckIfZero:
		lda 	XSControl,x
		bne 	_CAType
		lda 	XSNumber0,x
		ora 	XSNumber1,x
		ora 	XSNumber2,x
		ora 	XSNumber3,x
		rts

_CAType:
		.error_type

		.send code

