		.section code

Command_Comment: ;; [']

		lda 	(codePtr),y 				; optional string parameter
		cmp 	#KWD_SYS_STR
		bne 	_CRExit

		iny 								; skip over it, it's a comment.
		tya
		sec
		adc 	(codePtr),y
		tay

_CRExit:		
		rts
		
		.send code
				
