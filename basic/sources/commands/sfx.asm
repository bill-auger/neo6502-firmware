		.section code

Command_SFX:	;; [sfx]
		ldx 	#0
		jsr 	EXPEvalInteger8 			; channel #
		pha
		jsr 	ERRCheckComma
		jsr 	EXPEvalInteger8 			; f/x number
		sta 	ControlParameters+1
		pla
		sta 	ControlParameters+0
		.DoSendMessage 						; reset all sound
		.byte 	8,5
		rts		
		.send code

