; ************************************************************************************************
;
;									Reset graphics state
;
; ************************************************************************************************

GraphicsReset:
		stz 	graphicsPosX 				; position to 0,0
		stz 	graphicsPosX+1
		stz 	graphicsPosY
		stz 	graphicsPosY+1
		lda 	#1  						; move mode, size 1
		sta 	graphicsCurrent
		sta 	graphicsSize
		stz 	inkAndByte 					; colour draw white
		lda 	#7
		sta 	inkXorByte
		stz 	graphicsSolidMode 			; and frame mode.
		rts

		.send code

