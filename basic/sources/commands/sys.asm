		.section code

Command_Sys:	;; [sys]
		ldx 	#0
		jsr 	EXPEvalInteger16 			; execution address
		phy

		lda 	XSNumber0,x 				; copy address into subroutine call.
		sta 	_SysCall+1
		lda 	XSNumber1,x
		sta 	_SysCall+2

_SysCall:
		jsr 	$FFFF 						; call the code
		ply
		rts

	
		.send code
						
