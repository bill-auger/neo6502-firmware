UnaryNotes: ;; [notes(]	
		jsr 	EXPEvalInteger8 			; channel #
		sta 	ControlParameters+0 		; save as parameter
		jsr 	ERRCheckRParen 				; no parameter

		.DoSendMessage 						; get queue size.
		.byte 	8,6
		.DoWaitMessage
		lda 	ControlError
		bne 	_UNRange
		lda 	ControlParameters+0
		jmp 	EXPUnaryReturnA
_UNRange:
		.error_range			
		.send code

