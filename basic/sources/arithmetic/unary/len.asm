		.section code	

EXPUnaryLen: ;; [len(]
		jsr 	EXPEvalString 					; string to R0, zTemp0		
		jsr 	ERRCheckRParen 					; )
		lda 	(zTemp0) 						; first byte is the length
		;
		;		Return A as integer.
		;
EXPUnaryReturnA:		
		sta 	XSNumber0,x
		stz 	XSNumber1,x
		stz 	XSNumber2,x
		stz 	XSNumber3,x
		stz 	XSControl,x
		rts

		.send code

;: [len(string)]\
; Returns the length of the string\
; { print len("Hello") } prints 5

