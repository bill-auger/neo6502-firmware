		.section code	

ConstFalse: ;; [false]
		lda 	#0
		bra 	ConstCommon
ConstTrue: ;; [true]
		lda 	#$FF
ConstCommon:		
		sta 	XSNumber3,x
		sta 	XSNumber2,x
		sta 	XSNumber1,x
		sta 	XSNumber0,x
		stz 	XSControl,x
		rts
		
		.send code

				
