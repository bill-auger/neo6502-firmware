;
;									Check next character (General)
;
; ************************************************************************************************

		.section code	

checknext .macro
		lda 	(CodePtr),y
		cmp 	#\1
		bne 	ERRSyntaxError
		iny
		rts
		.endm

; ************************************************************************************************
;
;											Specific checks
;
; ************************************************************************************************

ERRCheckLParen:
		.checknext 	KWD_LPAREN
ERRCheckRParen:
		.checknext 	KWD_RPAREN
ERRCheckComma:
		.checknext 	KWD_COMMA

ERRCheckA:
		cmp 	(codePtr),y
		bne 	ERRSyntaxError
		iny
		rts		

ERRSyntaxError:
		.error_syntax

		.send code
		
