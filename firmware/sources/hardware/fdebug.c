#include "common.h"

static int fIsInitialised = 0;

// ***************************************************************************************
//
//							Send character to firmware debugger
//
// ***************************************************************************************

void FDBWrite(uint8_t c) {
	if (fIsInitialised == 0) { 				// Is the port open ?
		stdio_uart_init_full(uart0, 115200, 28, 29);
		fIsInitialised = -1;
	}
	putchar(c); 							// Send character to it.
}

