#include "common.h"

struct repeating_timer timer;

// ***************************************************************************************
//
// 							50Hz Callback function
//
// ***************************************************************************************

static bool Tick50Callback(struct repeating_timer *t) {
	TICKProcess();
    return true;
}

// ***************************************************************************************
//
//								Initialise callback
//
// ***************************************************************************************

void THWStart(void) {
    add_repeating_timer_ms(20, Tick50Callback, NULL, &timer);
}

